package es.andresfusteralonso.gimnasio_fct;

public class Monitor {
    private int idMO;
    private String nombreMO;
    private String apellidosMO;
    private String dniMO;
    private String telefonoMO;
    private String correoMO;
    private String sexoMO;
    private String contrato;

    public int getIdMO() {
        return idMO;
    }

    public void setIdMO(int idMO) {
        this.idMO = idMO;
    }

    public String getNombreMO() {
        return nombreMO;
    }

    public void setNombreMO(String nombreMO) {
        this.nombreMO = nombreMO;
    }

    public String getApellidosMO() {
        return apellidosMO;
    }

    public void setApellidosMO(String apellidosMO) {
        this.apellidosMO = apellidosMO;
    }

    public String getDniMO() {
        return dniMO;
    }

    public void setDniMO(String dniMO) {
        this.dniMO = dniMO;
    }

    public String getTelefonoMO() {
        return telefonoMO;
    }

    public void setTelefonoMO(String telefonoMO) {
        this.telefonoMO = telefonoMO;
    }

    public String getCorreoMO() {
        return correoMO;
    }

    public void setCorreoMO(String correoMO) {
        this.correoMO = correoMO;
    }

    public String getSexoMO() {
        return sexoMO;
    }

    public void setSexoMO(String sexoMO) {
        this.sexoMO = sexoMO;
    }

    public String getContrato() {
        return contrato;
    }

    public void setContrato(String contrato) {
        this.contrato = contrato;
    }
}